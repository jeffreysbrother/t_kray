      <p><b>Active Development:</b> 2014-2015</p>
        <img style="border: 5px solid #1e1e1e;" src="img/resistance_software_thumb.png">
        <p class="stubborn">I designed this software for internal use only as well, for engineers and manufacturing verification. The objective was to utilize the IEEE-488 (GPIB) interface associated with a very accurate but old digital multimeter using 4-wire resistance
