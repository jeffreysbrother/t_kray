      <h1>CUSTOM MODBUS TEST SOFTWARE:</h1>
      <p><b>Active Development:</b> 2014-2015</p>

          <img style="border: 5px solid #1e1e1e;" src="img/modbus_software_thumb.png">
          <br>
          <img style="border: 5px solid #1e1e1e;" src="img/modbus_software_linux_thumb.png">
          <p class="stubborn">
        I designed this software for internal use only, for myself and other test engineers. The objective was to utilize Modbus communication protocol in conjunction with any device capable of Modbus RTU communication. There are a lot of professional
        programs available for this, but none of them met my standards, and often had critical bugs preventing efficient testing, so I ended up writing my own.
        <p><b>Features:</b>
            <ul>
              <li>Cross-Platform (Linux/OSX)</li>
              <li>Modbus RTU Communication (Serial RS232/RS485)</li>
              <li>Complete COM Port Setup (COM Port/Baud Rate/Data Bits/Parity/Stop Bits/Slave ID)</li>
              <li>Module: Single/Multi-Read polling for 32-Bit Floating Point and 16-Bit Integer registers</li>
              <li>Module: Single/Multi-Write for 32-Bit Floating Point and 16-Bit Integer registers</li>
              <li>Efficiently formatted data logging option (csv), creates a new file every new day at midnight</li>
              <li>Module: Customizable failure threshold triggering (ex: if R7001 != 0 then output a failure message to log/console)</li>
              <li>Customizable register labling</li>
              <li>Byte value output</li>
              <li>Status Bar and Debug messaging</li>
              <li>Customizable test duration (minutes/read counts/write counts)</li>
              <li>Write random values to registers within custom set range</li>
              <li>Output terminal with an input command line</li>
              <li>Transmit LED indicators and itteration progress bar</li>
              <li>100% customized graphics and theme</li>
            </ul><b>Note:</b> This software was created internally for <a target="_blank" href="http://www.oleumtech.com">OleumTech Corporation</a> and is unavailable for public use.</div>
